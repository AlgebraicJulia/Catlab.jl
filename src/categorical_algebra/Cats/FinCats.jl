# FinCatGraph
#------------

""" Abstract type for category backed by finite generating graph.

Edges are presumably morphism generators, and paths are the morphisms.
"""
abstract type FinCatGraph{G,V,E} <: FinCatImpl{V,Path{V,E},E} end

@instance ThFinCat{V,Path{V,E},E,Any} [model::FinCatGraph{G,V,E}] where {G,V,E} begin
  dom(f::Path{V,E}) = get_ob(getvalue(model), src(f))

  codom(f::Path{V,E}) = get_ob(getvalue(model),tgt(f))

  id(x::V) = empty(Path, getvalue(model), x)

  compose(f::Path{V,E},g::Path{V,E}) = vcat(f, g)

  singleton(f::E) = Path(getvalue(model), f)

  ob_generators() = let m = getvalue(model); get_ob.(Ref(m), vertices(m)) end

  hom_generators() = let m = getvalue(model); get_hom.(Ref(m), edges(m)) end
end


function Base.show(io::IO, C::FinCatGraph)
  print(io, "FinCat(")
  show(io, getvalue(C))
  print(io, ", [")
  join(io, equations(C), ", ")
  print(io, "])")
end

decompose(::FinCatGraph, path::Path) = edges(path)

