# Categories
############

"""
A FinCat extends the category interface with nullary functions to list the 
ob/hom generators.

This interface does NOT specify the fact that any implementation of FinCat 
should specify `equations`.
"""
@theory ThFinCat <: ThCategory begin
  Gen(gen_dom::Ob,gen_codom::Ob)::TYPE
  Any′::TYPE 
  singleton(g::Gen(a,b))::Hom(a,b) ⊣ [(a,b)::Ob]
  ob_generators()::Any′  # Vector{Ob}
  hom_generators()::Any′ # Vector{Gen} 
end

const M{Ob,Hom,Gen} = Model{Tuple{Ob,Hom,Gen,Any}}

abstract type FinCatImpl{Ob,Hom,Gen} <: M{Ob,Hom,Gen} end

""" A finitely presented (but not necessarily finite!) category.
"""
@struct_hash_equal struct FinCat{Ob,Hom,Gen} <: AbsCat{Ob,Hom}
  impl::FinCatImpl{Ob,Hom,Gen}
  function FinCat(impl::FinCatImpl{Ob,Hom,Gen}) where {Ob,Hom,Gen}
    implements(impl, ThFinCat) || error("Model isn't a FinCat")
    new{Ob,Hom,Gen}(impl)
  end
end

getvalue(f::FinCat) = f.impl

Base.show(io::IO, C::FinCat) = show(io, getvalue(C))

equations(f::FinCat) = equations(getvalue(f))

equations(::FinCatImpl) = [] # by default, assume none

function dom(f::FinCat{Ob,Hom,Gen}, x::Gen)::Ob where {Ob,Hom,Gen} 
  dom(f, singleton(f, x))
end

function codom(f::FinCat{Ob,Hom,Gen}, x::Gen)::Ob where {Ob,Hom,Gen} 
  codom(f, singleton(f, x))
end

function singleton(f::FinCat{Ob,Hom,Gen}, x::Gen)::Hom where {Ob,Hom,Gen} 
  ThFinCat.singleton[getvalue(f)](x)
end

function compose(C::FinCat{Ob,Hom,Gen}, f::Hom, g::Gen) where {Ob,Hom,Gen} 
  compose(C, f, singleton(C,g))
end

function compose(C::FinCat{Ob,Hom,Gen}, f::Gen, g::Hom) where {Ob,Hom,Gen} 
  compose(C, singleton(C,f), g)
end

function compose(C::FinCat{Ob,Hom,Gen}, fs::Vector{Gen}) where {Ob,Hom,Gen} 
  compose(C, singleton.(Ref(C), fs))
end

function compose(C::FinCat{Ob,Hom,Gen}, f::Gen, g::Gen) where {Ob,Hom,Gen} 
  compose(C, singleton(C, f), singleton(C,g))
end

function ob_generators(f::FinCat{O,H,G})::AbstractVector{O} where {O,H,G} 
  ThFinCat.ob_generators[getvalue(f)]()
end

function hom_generators(f::FinCat{O,H,G})::AbstractVector{G} where {O,H,G} 
  ThFinCat.hom_generators[getvalue(f)]()
end

